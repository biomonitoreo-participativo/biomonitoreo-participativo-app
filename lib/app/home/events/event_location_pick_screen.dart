            onTap: () {
              setState(() {
                _pickedLocation = [-83.17880105587238, 9.729058926118755, 'ACLAC'];
              });
            },
          ),
        ],
      );
    }

    return Scaffold(
      appBar: AppBar(
        title: Text('Selección de localidad'),
        actions: <Widget>[
          IconButton(
            icon: Icon(Icons.check),
            onPressed: _pickedLocation == null
                ? null
                : () {
                    print(
                        '$_pickedLocation: ${_pickedLocation[0]} ${_pickedLocation[1]} ${_pickedLocation[2]}');
                    Navigator.of(context).pop(_pickedLocation);
                  },
          ),
        ],
      ),
      body: Stack(
        children: <Widget>[
          _buildGoogleMap(),
          _buildContainer(),
        ],
      ),
    );
  }

  Widget _buildGoogleMap() {
    return GoogleMap(
      mapType: MapType.hybrid,
      onMapCreated: _onMapCreated,
      markers: markers,
      polygons: _polygons,
      initialCameraPosition: CameraPosition(
        target: LatLng(
          widget.initialLocality[1],
          widget.initialLocality[0],
        ),
        zoom: 10,
      ),
    );
  }

  Widget _buildContainer() {
    return Positioned(
      top: 5.0,
      right: 15.0,
      left: 15.0,
      child: Container(
          height: 50.0,
          width: double.infinity,
          decoration: BoxDecoration(
              borderRadius: BorderRadius.circular(10.0), color: Colors.white),
          child: TypeAheadField(
            textFieldConfiguration: TextFieldConfiguration(
              autofocus: false,
              decoration: InputDecoration(
                hintText: 'Nombre de la localidad',
                border: InputBorder.none,
                contentPadding: EdgeInsets.only(left: 15.0, top: 15.0),
                suffixIcon: IconButton(
                    icon: IconButton(
                  icon: Icon(Icons.search),
                  onPressed: null,
                  iconSize: 30.0,
                )),
              ),
            ),
            suggestionsCallback: (pattern) {
              return LocationData.findByFilter(pattern);
            },
            itemBuilder: (context, suggestion) {
              return ListTile(
                leading: Icon(Icons.place),
                title: Text(suggestion.name),
                subtitle: Text('${suggestion.description}'),
              );
            },
            onSuggestionSelected: (suggestion) {
              // This when someone click the items
              print(suggestion);
              _gotoLocation(
                suggestion.decimalLatitude,
                suggestion.decimalLongitude,
              );
            },
          )),
    );
  }
